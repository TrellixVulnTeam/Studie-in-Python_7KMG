def check_pangram(text):

    alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

    text_set = set(text.lower())
    text_list = list(sorted(text_set))
    result = []

    for i in text_list:
        if i.isalpha():
            result.append(i)

    return alphabet == result

